// Helper function để lấy tỉnh theo ngày trong tuần
// dayOfWeek: 0 (Sunday) - 6 (Saturday)
export const getProvincesByDay = (dayOfWeek) => {
  const dayMap = {
    0: 'sunday',    // Chủ nhật
    1: 'monday',    // Thứ 2
    2: 'tuesday',   // Thứ 3
    3: 'wednesday', // Thứ 4
    4: 'thursday',  // Thứ 5
    5: 'friday',    // Thứ 6
    6: 'saturday'   // Thứ 7
  };

  const dayKey = dayMap[dayOfWeek];
  return provincesSchedule[dayKey];
};

// Lấy tỉnh cho ngày hiện tại
export const getTodayProvinces = () => {
  const today = new Date().getDay();
  return getProvincesByDay(today);
};

